public class Calculator {

	/**
	 * @param i
	 * @param j
	 * @return
	 */
	public static int add(int i, int j) {
		return i+j;
	}

	/**
	 * @param i
	 * @param j
	 * @return
	 */
	public static int sub(int i, int j) {
		return i-j;
	}

	/**
	 * @param i
	 * @param j
	 */
	public static float div(int i, int j) {
		return i/j;
		
	}

}
